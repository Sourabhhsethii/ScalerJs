/**
 * Arithmetic Operators
 */

let x = 10;
let y = 3;

// x + y => expression 
console.log(x+y);
console.log(x-y);
console.log(x*y);
console.log(x/y);
console.log(x%y);
console.log(x**y);

// Increment (++)

// First Incremnet then Display
console.log(++x);
// First Display then Increment
console.log(x++);
console.log(x);


// Decrement  (--)

// First Decrement then Display
console.log(--x);
// First Display then Decrement
console.log(x--);
console.log(x);

